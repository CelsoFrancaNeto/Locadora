    public void addFilmes() {
        String nome = jTextField1.getText();
        String categoria = jComboBox1.getSelectedItem().toString();
        String preco = jFormattedTextField1.getText();
        boolean estado = false;
        Filmes filme = new Filmes(nome, categoria, preco,estado);
        locadora.inserirFilme(filme);
        ArquivoTexto.writer(filme);
        indice++;
    }

