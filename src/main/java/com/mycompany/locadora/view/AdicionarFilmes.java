    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jComboBox1.getSelectedItem().equals("<Selecionar Categoria>")||jFormattedTextField1.getText().equals("")||jTextField1.getText().equals("")) {
            jLabel3.setForeground(Color.red);
            jLabel3.setText("Categoria inválida ou campo vazio!");
        } else {
            addFilmes();
            jTextField1.setText("");
            jFormattedTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jLabel3.setForeground(Color.green);
            jLabel3.setText("Filme adicionado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

