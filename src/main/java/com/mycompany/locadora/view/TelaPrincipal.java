    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (indice == 0) {
            for (int i = 0; i < locadora.getCatalogo().size(); i++) {
                Object[] dados = {locadora.getCatalogo().get(i).getNome(), locadora.getCatalogo().get(i).getCategoria(),  "R$"+locadora.getCatalogo().get(i).getPreco()};
                dtmFilmes.addRow(dados);
                if (locadora.getCatalogo().get(i).isEstado() == true) {
                    dtmFilmes.setValueAt(true, i, 3);
                }
                indice++;
            }

        } else if (indice < locadora.getCatalogo().size()) {
            for (int i = locadora.getCatalogo().size() - (locadora.getCatalogo().size() - indice); i < locadora.getCatalogo().size(); i++) {
                Object[] dados = {locadora.getCatalogo().get(i).getNome(), locadora.getCatalogo().get(i).getCategoria(), "R$"+locadora.getCatalogo().get(i).getPreco()};
                dtmFilmes.addRow(dados);
                indice++;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            if (locadora.getCatalogo().get(jTable1.getSelectedRow()).isEstado() == false) {
                dtmFilmes.setValueAt(true, jTable1.getSelectedRow(), 3);
                locadora.getCatalogo().get(jTable1.getSelectedRow()).setEstado(true);
                ArquivoTexto.cleaner();
                for (int i = 0; i < locadora.getCatalogo().size(); i++) {
                    Filmes filme = new Filmes(locadora.getCatalogo().get(i).getNome(), locadora.getCatalogo().get(i).getCategoria(), locadora.getCatalogo().get(i).getPreco(), locadora.getCatalogo().get(i).isEstado());
                    ArquivoTexto.writer(filme);
                }
            } else {
                dtmFilmes.setValueAt(false, jTable1.getSelectedRow(), 3);
                locadora.getCatalogo().get(jTable1.getSelectedRow()).setEstado(false);
                ArquivoTexto.cleaner();
                for (int i = 0; i < locadora.getCatalogo().size(); i++) {
                    Filmes filme = new Filmes(locadora.getCatalogo().get(i).getNome(), locadora.getCatalogo().get(i).getCategoria(), locadora.getCatalogo().get(i).getPreco(), locadora.getCatalogo().get(i).isEstado());
                    ArquivoTexto.writer(filme);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (loginSeason.getLoginSeason()) {
            if (jTable1.getSelectedRow() != -1) {
                locadora.getCatalogo().remove(jTable1.getSelectedRow());
                dtmFilmes.removeRow(jTable1.getSelectedRow());
                ArquivoTexto.cleaner();
                indice--;
                for (int i = 0; i < locadora.getCatalogo().size(); i++) {
                    Filmes filme = new Filmes(locadora.getCatalogo().get(i).getNome(), locadora.getCatalogo().get(i).getCategoria(), locadora.getCatalogo().get(i).getPreco(), locadora.getCatalogo().get(i).isEstado());
                    ArquivoTexto.writer(filme);
                }
            }
        }

    }//GEN-LAST:event_jToggleButton1ActionPerformed

